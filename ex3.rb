# + plus addition
# - minus subtraction
# / slash division
# * asterisk multiplication
# % percent modulus
# < less-than comparison operator
# > greater-than comparison operator
# <= less-than-equal comparison operator
# >= greater-than-equal comparison operator


# Prints a string
puts "I will now count my chickens:"


# Calculates and prints the number of hens 
puts "Hens #{25.0 + 30.0 / 6.0}"

# Calculates and prints the number of roosters
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}"

# Prints a string 
puts "Now I will count the eggs:"

# Performs math calculation and prints result
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0

# Prints a string that includes numbers and arithmetic / comparison operators
puts "Is it true that 3 + 2 < 5 - 7?"

# Performs math calculation and prints result
puts 3.0 + 2.0 < 5.0 - 7.0

# Prints a string that includes numbers and 
# arithmetic / comparison operators AND performs calculation
puts "What is 3 + 2? #{3.0 + 2.0}"
puts "What is 5 - 7? #{5.0 - 7.0}"

